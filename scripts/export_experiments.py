#!/usr/bin/env python3
"""
scripts/export_experiments.py
==============================
Reads all MLflow experiments from mlruns/ and produces:

  experiments/
    leaderboard.md                      ← master comparison table (all runs)
    exp<N>_<name>/
      params.json                       ← hyperparameters
      metrics_final.json                ← final / best metric values
      metrics_per_epoch.csv             ← step-by-step metric history
      summary.md                        ← human-readable run card

Usage (from repo root):
    python scripts/export_experiments.py
    python scripts/export_experiments.py --tracking_uri ./mlruns
"""

import argparse
import json
import csv
import os
import re
from pathlib import Path
from collections import defaultdict

import mlflow
from mlflow.tracking import MlflowClient

REPO_ROOT     = Path(__file__).resolve().parent.parent
MLRUNS_DIR    = REPO_ROOT / 'mlruns'
EXPERIMENTS_DIR = REPO_ROOT / 'experiments'

# Metrics we care about (in display order)
KEY_METRICS = ['auc_roc', 'accuracy', 'f1_macro', 'f1_weighted',
               'sensitivity', 'specificity', 'val_loss', 'train_loss', 'train_acc']


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _slug(name: str) -> str:
    """Convert run name to a safe folder name."""
    return re.sub(r'[^\w\-]', '_', name.lower())


def _get_history(client: MlflowClient, run_id: str, metric: str) -> list[dict]:
    """Return [{step, value, timestamp}] for a metric across all epochs."""
    history = client.get_metric_history(run_id, metric)
    return sorted(
        [{'step': m.step, 'value': round(m.value, 6), 'timestamp_ms': m.timestamp}
         for m in history],
        key=lambda x: x['step']
    )


def _final_val(client: MlflowClient, run_id: str, metric: str):
    """Return the best (max AUC / min loss) or last value for a metric."""
    history = _get_history(client, run_id, metric)
    if not history:
        return None
    if 'loss' in metric:
        return min(h['value'] for h in history)
    if metric in ('auc_roc', 'accuracy', 'f1_macro', 'f1_weighted',
                  'sensitivity', 'specificity', 'train_acc', 'best_auc_roc'):
        return max(h['value'] for h in history)
    return history[-1]['value']


def _write_summary_md(out_dir: Path, run_name: str, params: dict,
                      final_metrics: dict, n_epochs: int):
    lines = [
        f"# {run_name}",
        "",
        "## Hyperparameters",
        "",
        "| Parameter | Value |",
        "|-----------|-------|",
    ]
    for k, v in sorted(params.items()):
        lines.append(f"| `{k}` | `{v}` |")

    lines += [
        "",
        "## Results",
        "",
        f"Training epochs: **{n_epochs}**",
        "",
        "| Metric | Value |",
        "|--------|-------|",
    ]
    for m in KEY_METRICS + ['best_auc_roc']:
        v = final_metrics.get(m)
        if v is not None:
            lines.append(f"| `{m}` | **{v:.4f}** |")

    lines += [
        "",
        "## How to reproduce",
        "",
        "```bash",
        f"python -m src.models.training \\",
    ]
    for k, v in sorted(params.items()):
        flag = f"--{k}"
        if str(v).lower() in ('true', 'false'):
            if str(v).lower() == 'true':
                lines.append(f"  {flag} \\")
        else:
            lines.append(f"  {flag} {v} \\")
    lines[-1] = lines[-1].rstrip(' \\')
    lines.append("```")
    lines.append("")

    (out_dir / 'summary.md').write_text('\n'.join(lines))


def _write_leaderboard(experiments_dir: Path, runs: list[dict]):
    """Overwrite leaderboard.md with the full sorted comparison table."""
    runs_sorted = sorted(runs, key=lambda r: r['best_auc'], reverse=True)

    lines = [
        "# 🏆 Experiment Leaderboard",
        "",
        "> Auto-generated by `scripts/export_experiments.py` — do not edit manually.",
        "> Sorted by **AUC-ROC (macro OvR)** descending.",
        "",
        "| # | Experiment | Backbone | Epochs | AUC-ROC | Accuracy | F1-macro | Sensitivity | Specificity | Class Weights | MixUp | Adv Aug |",
        "|---|------------|----------|--------|---------|----------|----------|-------------|-------------|:---:|:---:|:---:|",
    ]
    for r in runs_sorted:
        p = r['params']
        m = r['metrics']
        lines.append(
            f"| {r['rank']} | [{r['name']}](./{r['folder']}/summary.md) "
            f"| `{p.get('backbone','?')}` "
            f"| {p.get('epochs','?')} "
            f"| **{m.get('auc_roc', 0):.4f}** "
            f"| {m.get('accuracy', 0):.4f} "
            f"| {m.get('f1_macro', 0):.4f} "
            f"| {m.get('sensitivity', 0):.4f} "
            f"| {m.get('specificity', 0):.4f} "
            f"| {'✅' if str(p.get('use_class_weights','')).lower()=='true' else '❌'} "
            f"| {'✅' if str(p.get('use_mixup','')).lower()=='true' else '❌'} "
            f"| {'✅' if str(p.get('use_advanced_aug','')).lower()=='true' else '❌'} |"
        )

    lines += [
        "",
        "---",
        "",
        "## Metric definitions",
        "",
        "| Metric | Definition |",
        "|--------|------------|",
        "| **AUC-ROC** | One-vs-rest macro-averaged Area Under ROC Curve — primary metric |",
        "| **Accuracy** | % of correct predictions across all classes |",
        "| **F1-macro** | Unweighted average F1 score across 7 classes |",
        "| **Sensitivity** | Macro-averaged recall (true positive rate) |",
        "| **Specificity** | Macro-averaged true negative rate |",
        "",
    ]

    (experiments_dir / 'leaderboard.md').write_text('\n'.join(lines))
    print(f"  → wrote {experiments_dir}/leaderboard.md")


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def export(tracking_uri: str = str(MLRUNS_DIR)):
    mlflow.set_tracking_uri(tracking_uri)
    client = MlflowClient(tracking_uri=tracking_uri)

    EXPERIMENTS_DIR.mkdir(exist_ok=True)
    print(f"[export] Reading from: {tracking_uri}")

    all_experiments = client.search_experiments()
    print(f"[export] Found {len(all_experiments)} experiment(s)")

    run_summaries = []

    for exp in all_experiments:
        if exp.name == 'Default':
            continue

        runs = client.search_runs(experiment_ids=[exp.experiment_id])
        if not runs:
            continue

        # Use the most recent run for each experiment name
        run = sorted(runs, key=lambda r: r.info.start_time, reverse=True)[0]
        run_id   = run.info.run_id
        run_name = exp.name     # we use exp name as the canonical name

        print(f"[export] Processing: {run_name}  (run_id={run_id[:8]}...)")

        # ── Params ──────────────────────────────────────────────────────
        params = run.data.params

        # ── Final metrics ────────────────────────────────────────────────
        final_metrics = {}
        all_metric_keys = list(run.data.metrics.keys())
        for m in all_metric_keys:
            v = _final_val(client, run_id, m)
            if v is not None:
                final_metrics[m] = round(v, 6)

        # ── Per-epoch history ────────────────────────────────────────────
        per_epoch: dict[int, dict] = defaultdict(dict)
        epoch_metrics = [k for k in all_metric_keys if k not in ('best_auc_roc',)]
        for m in epoch_metrics:
            for h in _get_history(client, run_id, m):
                per_epoch[h['step']][m] = h['value']

        n_epochs = max(per_epoch.keys()) if per_epoch else int(params.get('epochs', 0))

        # ── Output folder ────────────────────────────────────────────────
        # Name like "exp01_resnet50_baseline" from exp name
        folder_name = _slug(run_name)
        out_dir = EXPERIMENTS_DIR / folder_name
        out_dir.mkdir(exist_ok=True)

        # params.json
        (out_dir / 'params.json').write_text(
            json.dumps(params, indent=2, sort_keys=True)
        )

        # metrics_final.json
        (out_dir / 'metrics_final.json').write_text(
            json.dumps(final_metrics, indent=2, sort_keys=True)
        )

        # metrics_per_epoch.csv
        if per_epoch:
            steps = sorted(per_epoch.keys())
            all_cols = sorted({col for row in per_epoch.values() for col in row})
            with open(out_dir / 'metrics_per_epoch.csv', 'w', newline='') as f:
                writer = csv.DictWriter(f, fieldnames=['epoch'] + all_cols)
                writer.writeheader()
                for step in steps:
                    row = {'epoch': step}
                    row.update({k: round(per_epoch[step].get(k, float('nan')), 6)
                                for k in all_cols})
                    writer.writerow(row)

        # summary.md
        _write_summary_md(out_dir, run_name, params, final_metrics, n_epochs)

        best_auc = final_metrics.get('auc_roc', 0.0)
        run_summaries.append({
            'name':   run_name,
            'folder': folder_name,
            'params': params,
            'metrics': final_metrics,
            'best_auc': best_auc,
            'rank': 0,   # filled after sort
        })
        print(f"  → {out_dir.relative_to(REPO_ROOT)}/  (AUC={best_auc:.4f})")

    # Rank by AUC
    for i, r in enumerate(sorted(run_summaries, key=lambda x: x['best_auc'], reverse=True), 1):
        r['rank'] = i

    if run_summaries:
        _write_leaderboard(EXPERIMENTS_DIR, run_summaries)

    print(f"\n[export] Done. {len(run_summaries)} run(s) exported to experiments/")


if __name__ == '__main__':
    parser = argparse.ArgumentParser()
    parser.add_argument('--tracking_uri', default=str(MLRUNS_DIR))
    args = parser.parse_args()
    export(args.tracking_uri)
